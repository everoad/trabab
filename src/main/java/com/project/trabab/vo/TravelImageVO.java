package com.project.trabab.vo;

public class TravelImageVO {

	private String travel_image_no;
	private String travel_image_name;
	private String travel_no;
	private String travel_step;

	public String getTravel_image_no() {
		return travel_image_no;
	}

	public void setTravel_image_no(String travel_image_no) {
		this.travel_image_no = travel_image_no;
	}

	public String getTravel_image_name() {
		return travel_image_name;
	}

	public void setTravel_image_name(String travel_image_name) {
		this.travel_image_name = travel_image_name;
	}

	public String getTravel_no() {
		return travel_no;
	}

	public void setTravel_no(String travel_no) {
		this.travel_no = travel_no;
	}

	public String getTravel_step() {
		return travel_step;
	}

	public void setTravel_step(String travel_step) {
		this.travel_step = travel_step;
	}

}
